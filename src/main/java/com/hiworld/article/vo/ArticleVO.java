package com.hiworld.article.vo;

public class ArticleVO {

	private int ArticleSerial;
	private String ArticleImg;
	private String ArticleKinds;
	private String ArticleName;
	private int ArticlePrice;
	private int UserSerial;
	private String ArticleContent;
	private String ArticleList;
	
	public int getArticleSerial() {
		return ArticleSerial;
	}
	public void setArticleSerial(int articleSerial) {
		ArticleSerial = articleSerial;
	}
	public String getArticleImg() {
		return ArticleImg;
	}
	public void setArticleImg(String articleImg) {
		ArticleImg = articleImg;
	}
	public String getArticleKinds() {
		return ArticleKinds;
	}
	public void setArticleKinds(String articleKinds) {
		ArticleKinds = articleKinds;
	}
	public String getArticleName() {
		return ArticleName;
	}
	public void setArticleName(String articleName) {
		ArticleName = articleName;
	}
	public int getArticlePrice() {
		return ArticlePrice;
	}
	public void setArticlePrice(int articlePrice) {
		ArticlePrice = articlePrice;
	}
	public int getUserSerial() {
		return UserSerial;
	}
	public void setUserSerial(int userSerial) {
		UserSerial = userSerial;
	}
	public String getArticleContent() {
		return ArticleContent;
	}
	public void setArticleContent(String articleContent) {
		ArticleContent = articleContent;
	}
	public String getArticleList() {
		return ArticleList;
	}
	public void setArticleList(String articleList) {
		ArticleList = articleList;
	}
	
	
	
}
